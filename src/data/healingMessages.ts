let lastIndex = -1;

export function getRandomHealingMessage(): string {
  let index: number;
  do {
    index = Math.floor(Math.random() * HEALING_MESSAGES.length);
  } while (index === lastIndex && HEALING_MESSAGES.length > 1);
  lastIndex = index;
  return HEALING_MESSAGES[index];
}
